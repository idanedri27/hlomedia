            // יצירת הודעה
            var message = 'שלום, שמי ' + name + '. מספר הטלפון שלי הוא ' + phone + '. הגעתי דרך אתר הלו-מדיה ואשמח לקבל ייעוץ חינם.';

            // קידוד ההודעה ל-URL
